public class sswitch {
    public static void main(String[] args) {
        // int n=3;
        char c='j';
        switch(c){
            case 's' : System.out.println("samosa");
            break;

            case 'j' : System.out.println("juice");
            break;

            case 'b': System.out.println("biryani");
            break;

            default: System.out.println("dreaming");
             
        }
    }
}
